
import { toast } from "react-hot-toast";
import { studentEndpoints } from "../apis";
import { apiConnector } from "../apiconnector";
import { setPaymentLoading } from "../../Slices/courseSlice";
import { resetCart } from "../../Slices/cartSlice";


const {COURSE_PAYMENT_API, COURSE_VERIFY_API, SEND_PAYMENT_SUCCESS_EMAIL_API} = studentEndpoints;

function loadScript(src) {
    return new Promise((resolve) => {
        const script = document.createElement("script");
        script.src = src;

        script.onload = () => {
            resolve(true);
        }
        script.onerror= () =>{
            resolve(false);
        }
        document.body.appendChild(script);
    })
}


export async function buyCourse(token, courses, userDetails, navigate, dispatch) {
    const toastId = toast.loading("Loading...");
    try{
        if (!token) {
            throw new Error("Please login to buy this course");
        }
        if (!Array.isArray(courses) || courses.length === 0) {
            throw new Error("No course selected");
        }
        const courseIds = courses.filter(Boolean);
        if (courseIds.length === 0) {
            throw new Error("Invalid course selected");
        }
        //load the script
        const res = await loadScript("https://checkout.razorpay.com/v1/checkout.js");

        if(!res) {
            toast.error("RazorPay SDK failed to load");
            return;
        }

        //initiate the order
        const orderResponse = await apiConnector("POST", COURSE_PAYMENT_API, 
                                { courses: courseIds },
                                {
                                    Authorization: `Bearer ${token}`,
                                })

        if(!orderResponse.data.success) {
            throw new Error(orderResponse.data.message);
        }
        console.log("PRINTING orderResponse", orderResponse);
        const orderData = orderResponse?.data?.data;
        if (!orderData?.id || !orderData?.amount || !orderData?.currency) {
            throw new Error("Invalid payment order response");
        }
        //options
        const razorpayKey = import.meta.env.VITE_RAZORPAY_KEY;
        if (!razorpayKey) {
            throw new Error("Missing VITE_RAZORPAY_KEY");
        }
        const options = {
            key: razorpayKey,
            currency: orderData.currency,
            amount: `${orderData.amount}`,
            order_id: orderData.id,
            name:"StudyNotion",
            description: "Thank You for Purchasing the Course",
            prefill: {
                name: `${userDetails?.firstName || ""} ${userDetails?.lastName || ""}`.trim() || "Learner",
                email: userDetails?.email || "",
            },
            handler: function(response) {
                //send successful wala mail
                sendPaymentSuccessEmail(response, orderData.amount, token);
                //verifyPayment
                verifyPayment({...response, courses: courseIds}, token, navigate, dispatch);
            }
        }
        //miss hogya tha 
        const paymentObject = new window.Razorpay(options);
        paymentObject.open();
        paymentObject.on("payment.failed", function(response) {
            toast.error("oops, payment failed");
            console.log(response.error);
        })

    }
    catch(error) {
        console.log("PAYMENT API ERROR.....", error);
        const message =
            error?.response?.data?.message ||
            error?.response?.data?.error ||
            error?.message ||
            "Could not make Payment";
        const backendDown =
            error?.message === "Network Error" ||
            error?.code === "ERR_NETWORK" ||
            error?.code === "ECONNREFUSED";
        toast.error(backendDown ? "Backend server is not running on port 3500" : message);
    }
    toast.dismiss(toastId);
}

async function sendPaymentSuccessEmail(response, amount, token) {
    try{
        await apiConnector("POST", SEND_PAYMENT_SUCCESS_EMAIL_API, {
            orderId: response.razorpay_order_id,
            paymentId: response.razorpay_payment_id,
            amount,
        },{
            Authorization: `Bearer ${token}`
        })
    }
    catch(error) {
        console.log("PAYMENT SUCCESS EMAIL ERROR....", error);
    }
}

//verify payment
async function verifyPayment(bodyData, token, navigate, dispatch) {
    const toastId = toast.loading("Verifying Payment....");
    dispatch(setPaymentLoading(true));
    try{
        const response  = await apiConnector("POST", COURSE_VERIFY_API, bodyData, {
            Authorization:`Bearer ${token}`,
        })

        if(!response.data.success) {
            throw new Error(response.data.message);
        }
        toast.success("Payment successful. You are added to the course");
        navigate("/dashboard/enrolled-courses");
        dispatch(resetCart());
    }   
    catch(error) {
        console.log("PAYMENT VERIFY ERROR....", error);
        toast.error("Could not verify Payment");
    }
    toast.dismiss(toastId);
    dispatch(setPaymentLoading(false));
}
